                <ul>
                    <li>1. 컴모아 : 회사가 운영하는 인터넷사이트(commoa.com)로서, 이용자에게 컴퓨터 등 정보통신설비를 통하여 컴퓨터 관련 하드웨어 상품에 대한 정보를 제공하는 가상의 영업장을 말합니다.</li>
                    <li>2. 회원 : 회사에 개인정보를 제공하여 회원 등록을 한 자로서, 회사의 정보를 지속적으로 제공받으며, 회사가 제공하는 서비스를 계속적으로 이용할 수 있는 자를 말합니다.</li>
                    <li>3. 이용자 : 컴모아에 접속하여 이 약관에 따라 회사가 제공하는 서비스를 받는 회원 및 비회원을 말합니다.</li>
                    <li>4. 비회원 : 회원에 가입하지 않고 회사가 제공하는 서비스를 이용하는 자를 말합니다.</li>
                    <li>5. 아이디(e-mail) : 회원의 식별과 서비스 이용을 위하여 회원이 설정하고 회사가 승인하여 등록된 이메일 형식의 문자 조합을 말합니다.</li>
                    <li>6. 비밀번호(Password) : 회원의 동일성 확인과 회원의 권익 및 비밀보호를 위하여 회원 스스로가 설정하여 회사에 등록한 영문과 숫자의 조합을 말합니다.</li>
                    <li>7. 제휴사 : 컴모아와 계약 하에 상품의 노출 및 광고를 목적으로 컴모아에 정보를 제공하고 있는 컴퓨터 하드웨어 판매 사이트를 말합니다.</li>
                    <li>8. 상품 : 컴모아에서 정보를 제공하는 제휴사의 컴퓨터 하드웨어 상품을 말합니다.</li>
                </ul>
            </li>
