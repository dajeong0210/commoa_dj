            <li>회사는 약관의 규제에 관한 법률, 전자문서 및 전자거래 기본법, 전자서명법, 정보통신망 이용 촉진 및 정보보호등에 관한 법률(이하 “정보통신망법”이라 함) 등 관련법을 위배하지 않는 범위에서 본 약관을 개정할 수 있습니다.</li>
